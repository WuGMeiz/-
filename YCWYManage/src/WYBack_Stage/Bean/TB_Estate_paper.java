package WYBack_Stage.Bean;

public class TB_Estate_paper {
	
	
	private int pa_id;	//	编号，主键
	private int es_id;	//	小区ID
	private String bankid;	//	物业订单id
	private int paper_num;	//	票据号
	private int status;	//	状态（0 删除  1 正常）
	private String create_time;	//	创建时间
	private int print_status;  //0 代表 已打印  1代表  未打印
	

	public TB_Estate_paper() {
		super();
	}
	public TB_Estate_paper(int pa_id, int es_id, String bankid, int paper_num,
			int status, String create_time, int print_status) {
		super();
		this.pa_id = pa_id;
		this.es_id = es_id;
		this.bankid = bankid;
		this.paper_num = paper_num;
		this.status = status;
		this.create_time = create_time;
		this.print_status = print_status;
	}
	
	
	public int getPrint_status() {
		return print_status;
	}
	public void setPrint_status(int print_status) {
		this.print_status = print_status;
	}
	public int getPa_id() {
		return pa_id;
	}
	public void setPa_id(int pa_id) {
		this.pa_id = pa_id;
	}
	public int getEs_id() {
		return es_id;
	}
	public void setEs_id(int es_id) {
		this.es_id = es_id;
	}
	public int getPaper_num() {
		return paper_num;
	}
	public void setPaper_num(int paper_num) {
		this.paper_num = paper_num;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	public String getCreate_time() {
		return create_time;
	}
	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}
	public String getBankid() {
		return bankid;
	}
	public void setBankid(String bankid) {
		this.bankid = bankid;
	}
	@Override
	public String toString() {
		return "TB_Estate_paper [pa_id=" + pa_id + ", es_id=" + es_id
				+ ", bankid=" + bankid + ", paper_num=" + paper_num
				+ ", status=" + status + ", create_time=" + create_time
				+ ", print_status=" + print_status + "]";
	}


	
	
	
	

	

}
